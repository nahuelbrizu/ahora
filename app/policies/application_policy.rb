class ApplicationPolicy
  attr_reader :request_organization, :user, :record

  def initialize(authorization_context, record)
    @user = authorization_context.user
    @organization = authorization_context.organization
    @record = record
  end

  def index?
    false
    # Your policy has access to @user, @organization, and @record.
  end

  def show?
    scope.where(:id => record.id).exists?
  end

  def create?
    false
  end

  def new?
    create?
  end

  def update?
    false
  end

  def edit?
    update?
  end

  def destroy?
    false
  end

  def scope
    Pundit.policy_scope!(user, record.class)
  end

  class Scope
    attr_reader :user, :scope

    def initialize(user, scope)
      @user = user
      @scope = scope
    end

    def resolve
      scope
    end
  end
end

class PostPolicy
  attr_reader :user, :post

  def initialize(user, post)
    @user = user
    @post = post
  end

  def update?
    user.admin? or not post.published?
  end


end
